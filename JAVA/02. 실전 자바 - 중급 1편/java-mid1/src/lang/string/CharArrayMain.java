package lang.string;

public class CharArrayMain {

    public static void main(String[] args) {
        char a = 'a';
        System.out.println("a = " + a);

        char[] charArr = new char[]{'h', 'e', 'l', 'l', 'o' };
        System.out.println(charArr);

        String str = "hello";
        System.out.println(str);
    }

}
